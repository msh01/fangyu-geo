export function getModernRegion(name: string) {
  const exact = modernRegions[name];
  if (exact) return exact;

  const slashPart = name.split(/[／/]/).find((part) => modernRegions[part]);
  if (slashPart) return modernRegions[slashPart];

  return undefined;
}
